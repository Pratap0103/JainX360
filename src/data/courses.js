// Helper function to get all unique locations
export const getAllLocations = () => {
  const locations = new Set();
  courses.forEach(course => {
    course.locations.forEach(loc => {
      locations.add(loc.place);
    });
  });
  return Array.from(locations);
};

// Helper function to get courses by location
export const getCoursesByLocation = (location) => {
  return courses.filter(course => 
    course.locations.some(loc => loc.place === location)
  );
};

// Helper function to get all available dates
export const getAllDates = () => {
  const dates = [];
  courses.forEach(course => {
    course.locations.forEach(loc => {
      dates.push({
        program: course.title,
        location: loc.place,
        dates: loc.dates
      });
    });
  });
  return dates.sort((a, b) => new Date(a.dates.split('-')[0]) - new Date(b.dates.split('-')[0]));
};
